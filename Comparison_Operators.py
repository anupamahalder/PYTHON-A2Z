# Comparison Operator
#-------------Equal------------------------
a = 5
b = 5
print(a==b) #True

#-------------Not Equal--------------------
print(a!=b) #False

#------------Greater than------------------
print(a>b) #False

#------------Less than---------------------
print(a<b) #False

#------------Greater than or equal to------
print(a>=b) #True

#------------Less than or equal to---------
print(a<=b) #True
