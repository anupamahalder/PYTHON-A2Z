# -----------------string type data----------------------
book = "Hello World"
print(type(book))

# -----------------number type data----------------------
num1 = 45
print(type(num1))

# -----------------floating type-------------------------
num2 = 34.9
print(type(num2))

# ----------------complex datatype----------------------- 
num = 42j
print(num)
print(type(num))

# ----------------boolean datatype-----------------------
Bool = True
print(Bool)
print(type(Bool))
print(7==9) #False

